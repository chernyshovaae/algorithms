"""
3.	По введенным пользователем координатам двух точек
вывести уравнение прямой вида y = kx + b, проходящей через эти точки.
"""

x1 = int(input('Введите координату x1:\n'))
y1 = int(input('Введите координату y1:\n'))
x2 = int(input('Введите координату x2:\n'))
y2 = int(input('Введите координату y2:\n'))

k = round((y1 - y2) / (x1 - x2), 2)
b = round(y2 - (k * x2), 2)
print(f'y = {k}x + {b}')
